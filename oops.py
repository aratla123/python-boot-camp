class Myclass:
    def add(a,b):
        return a+b
    def sub(a,b):
        return a-b
    def mul(a,b):
        return a*b
    def div(a,b):
        return a//b
    def mod(a,b):
        return a%b
    
obj1=Myclass
obj2=Myclass
print(obj1.mod(2,5))
print(obj1.div(9,7))
print(obj2.mul(21,15))
print(obj2.sub(4,5))
