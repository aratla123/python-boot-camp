#converting to uppercase

input="Hello"
print(input.upper())

#converting to lowercase

input="Hello"
print(input.lower())

#converting to titlecase

input="Hello"
print(input.title())

#converting swappingcase

input="Hello"
print(input.swapcase())

#converting to replace 

input="Hello"
print(input.replace('e','l'))

#spliting and strip
input="      hello world   "
print(input.split())
print(input.split('a'))#represents in  form of list with in square braces
print(input.strip())# removes the front and back space of a string

#swapping
input="Hello"
